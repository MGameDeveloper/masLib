void parse_comps(const char* comps)
{
	char buf[256];
	strcpy(buf, comps);

	char* token = strtok(buf, ",");
	while (token != NULL)
	{
		while (*token == ' ')
			token++;

		printf("TOKEN: %s\n", token);
		token = strtok(buf, ",");
	}
}

