bool mas_ecs_init()
{
    if(!mas_ecs_memory_init())
        return false;
    if(!mas_ecs_components_init())
        return false;

    return true;
}

void mas_ecs_deinit()
{
    mas_ecs_components_deinit();
    mas_ecs_memory_deinit();
}

void mas_ecs_update()
{
    mas_ecs_memory_frame_reset();
}